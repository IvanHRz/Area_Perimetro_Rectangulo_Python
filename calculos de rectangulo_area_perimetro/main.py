#Calcular area y perimetro de un rectangulo

altura = float(input("Ingrese el valor de la altura del rectangulo: "))
base = float(input("Ingrese el valor del ancho del rectangulo: "))

area = altura * base

print(f"El valor del area del rectangulo es: {area}")

perimetro= (altura + base) * 2

print(f"El valor del perimetro es: {perimetro}")
